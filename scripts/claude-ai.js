/**
 * claude-ai.js
 *
 * Real-Claude implementation of the two *analytical* document tasks that the
 * app uses. It is a drop-in companion to scripts/mock-ai.js:
 *
 *   generateDocuments(interview) -> { [filename]: content }   (async)
 *   analyzeCompany(interviews)   -> markdown string            (async)
 *   isEnabled()                  -> boolean
 *
 * Design:
 *   - The 3 *mechanical* documents (workflow.md / workflow.csv /
 *     workflow-diagram.md) are pure formatting of the interview answers, so we
 *     keep generating them with mock-ai (free, deterministic, identical layout).
 *   - The 3 *analytical* documents (job-description.md / kpi.md /
 *     optimization.md) and the company-wide report are generated by Claude
 *     (model: Sonnet 4.6) because that is where real reasoning adds value.
 *   - Prompt caching is applied to the (stable) system prompts so that running
 *     many employees in a row only pays for the system prompt once.
 *   - Every Claude call is wrapped so that ANY failure (no SDK installed,
 *     missing/invalid key, network error, malformed output) silently falls back
 *     to mock-ai. The app must never break because of the AI layer.
 *
 * Output language: Thai only (matches mock-ai).
 *
 * Configuration (env):
 *   ANTHROPIC_API_KEY   required to enable Claude. Absent -> falls back to mock.
 *   CLAUDE_MODEL        optional override (default: claude-sonnet-4-6)
 */

const mock = require('./mock-ai');

const MODEL = process.env.CLAUDE_MODEL || 'claude-sonnet-4-6';

// ------------------------------------------------------------------
// Lazy SDK / client init — requiring this file must never throw, even
// if @anthropic-ai/sdk isn't installed yet or the key is missing.
// ------------------------------------------------------------------
let _client = null;
let _initTried = false;

function getClient() {
  if (_client) return _client;
  if (_initTried) return null; // already failed once — don't retry require on every call
  _initTried = true;
  try {
    const sdk = require('@anthropic-ai/sdk');
    const Anthropic = sdk.Anthropic || sdk.default || sdk;
    _client = new Anthropic(); // reads ANTHROPIC_API_KEY from env
    return _client;
  } catch (e) {
    console.error('[claude-ai] SDK not available — using mock-ai. (' + e.message + ')');
    return null;
  }
}

function isEnabled() {
  return !!process.env.ANTHROPIC_API_KEY;
}

// ------------------------------------------------------------------
// Helpers
// ------------------------------------------------------------------
const pad = (n) => String(n).padStart(2, '0');

function getAnswer(interview, key) {
  const a = (interview.answers || []).find((x) => x.key === key);
  return a ? a.value : '';
}

function trunc(s, n) {
  const str = String(s || '').trim();
  return str.length > n ? str.slice(0, n) + '…' : str;
}

// Today in YYYY-MM-DD (Christian Era), from the server clock — passed into the
// prompts so Claude never has to guess/hallucinate the document date.
function todayStr() {
  const d = new Date();
  return `${d.getFullYear()}-${pad(d.getMonth() + 1)}-${pad(d.getDate())}`;
}

// Run a Claude message and return the concatenated text of the response.
// Uses streaming + finalMessage() so long outputs never hit a request timeout.
async function runClaude({ system, user, maxTokens = 8000 }) {
  const client = getClient();
  if (!client) throw new Error('claude client unavailable');

  const stream = client.messages.stream({
    model: MODEL,
    max_tokens: maxTokens,
    thinking: { type: 'adaptive' },
    output_config: { effort: 'medium' },
    system: [{ type: 'text', text: system, cache_control: { type: 'ephemeral' } }],
    messages: [{ role: 'user', content: user }],
  });

  const msg = await stream.finalMessage();

  if (msg.stop_reason === 'refusal') {
    throw new Error('claude refused the request');
  }

  const text = (msg.content || [])
    .filter((b) => b.type === 'text')
    .map((b) => b.text)
    .join('')
    .trim();

  if (!text) throw new Error('claude returned empty text');
  return text;
}

