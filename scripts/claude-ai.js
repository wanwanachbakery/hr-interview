// Build the per-employee context block from the interview answers
// ------------------------------------------------------------------
function buildEmployeeContext(interview) {
  const e = interview.employee || {};
  const lines = [];
  lines.push(`วันที่จัดทำ (ใช้วันที่นี้เท่านั้น): ${todayStr()}`);
  lines.push('');
  lines.push('ข้อมูลพนักงาน:');
  lines.push(`- ชื่อ: ${e.name || '-'}`);
  lines.push(`- ตำแหน่ง: ${e.role || '-'}`);
  lines.push(`- ฝ่าย/แผนก: ${e.department || e.division_name || e.division_id || '-'}`);
  lines.push(`- ภาษาที่ใช้สัมภาษณ์: ${String(interview.lang || 'th').toUpperCase()}`);
  lines.push('');
  lines.push(`บทบาทโดยสรุป (จากคำตอบ): ${getAnswer(interview, 'warmup') || '(ไม่ระบุ)'}`);
  lines.push('');

  // Time content — prefer the new hourly schema, fall back to the legacy schema.
  const hourLines = [];
  for (let h = 0; h < 24; h++) {
    const v = getAnswer(interview, `hour_${h}`);
    if (v) hourLines.push(`- ${pad(h)}:00–${pad(h + 1)}:00 → ${v}`);
  }
  if (hourLines.length) {
    lines.push('ตารางงานรายชั่วโมง:');
    lines.push(...hourLines);
  } else {
    const legacy = [
      ['morning_first', '30 นาทีแรกของวัน'],
      ['morning_main', 'งานหลักช่วงสาย (09:30–12:00)'],
      ['morning_tools', 'เครื่องมือ/ระบบช่วงเช้า'],
      ['morning_people', 'ประสานงานช่วงเช้ากับ'],
      ['noon_break', 'พักเที่ยง'],
      ['afternoon_main', 'งานบ่าย (13:00–16:00)'],
      ['afternoon_reports', 'รายงานประจำวัน'],
      ['afternoon_deadline', 'Deadline รายวัน'],
      ['evening_closing', 'ก่อนเลิกงาน'],
    ];
    const legacyLines = legacy
      .map(([k, label]) => {
        const v = getAnswer(interview, k);
        return v ? `- ${label}: ${v}` : null;
      })
      .filter(Boolean);
    if (legacyLines.length) {
      lines.push('ตารางงานประจำวัน:');
      lines.push(...legacyLines);
    }
  }
  lines.push('');

  const tail = [
    ['weekly_tasks', 'งานรายสัปดาห์/รายเดือน'],
    ['pain_points', 'งานที่เสียเวลา/น่าเบื่อ/ทำซ้ำที่สุด'],
    ['bottlenecks', 'คอขวด — รออะไรจากคน/ระบบอื่น'],
    ['own_kpi', 'ตัวชี้วัดในมุมพนักงานเอง'],
    ['ai_wishlist', 'งานที่พนักงานอยากให้ AI ช่วย'],
  ];
  for (const [k, label] of tail) {
    const v = getAnswer(interview, k);
    if (v) lines.push(`${label}: ${v}`);
  }

  const divAns = (interview.answers || []).filter((a) => a.key.startsWith('div_'));
  if (divAns.length) {
    lines.push('');
    lines.push('ข้อมูลเฉพาะฝ่าย:');
    for (const a of divAns) lines.push(`- ${a.value}`);
  }

  if (interview.worklogSummary) {
    lines.push('');
    lines.push('— ข้อมูลจากบันทึกงานประจำวันจริง (สำคัญ: ใช้ประกอบการวิเคราะห์ โดยเฉพาะ KPI และ Optimization — งานที่ทำซ้ำบ่อยคือเป้าหมายการลดงาน/ใช้ AI) —');
    lines.push(interview.worklogSummary);
  }

  return lines.join('\n');
}

