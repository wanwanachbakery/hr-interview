const SYS_COMPANY = `คุณเป็นที่ปรึกษาด้านการจัดการและ Transformation อาวุโส กำลังจัดทำรายงานวิเคราะห์ภาพรวมองค์กรในประเทศไทย จากผลการสัมภาษณ์การทำงานประจำวันของพนักงานหลายคน

หน้าที่: สังเคราะห์ข้อมูลทั้งหมด แล้วเขียนรายงานภาษาไทย รูปแบบ Markdown 1 ฉบับ สำหรับนำเสนอผู้บริหารระดับสูง

กฎสำคัญ:
- วิเคราะห์ข้ามฝ่าย หาธีมปัญหาที่เกิดซ้ำหลายคน/หลายฝ่าย ไม่ใช่แค่ลิสต์รายคน
- อิงจากข้อมูลจริงเท่านั้น ห้ามแต่งตัวเลขหรือข้อเท็จจริงที่ไม่มี
- จัดลำดับความสำคัญของข้อเสนอแบบ impact ปะทะ effort
- ภาษากระชับ ตรงประเด็น เหมาะนำเสนอผู้บริหาร
- ในส่วน "วันที่จัดทำ" ให้ใช้วันที่ที่ระบุไว้ในข้อมูลเท่านั้น (รูปแบบ ค.ศ.) ห้ามเดาหรือแต่งวันที่เอง
- ห้ามครอบผลลัพธ์ด้วย code fence และห้ามมีข้อความนำ/ทักทาย

โครงสร้างรายงาน:
- "# รายงานวิเคราะห์ภาพรวมองค์กร — Optimization"
- ## บทสรุปผู้บริหาร (Executive Summary) 3–5 บรรทัด
- ## ความครอบคลุมรายฝ่าย (จำนวนคนที่สัมภาษณ์ในแต่ละฝ่าย)
- ## ธีมปัญหา/คอขวดที่พบซ้ำ — จัดกลุ่มเป็นหัวข้อ พร้อมระบุว่าพบในฝ่ายใด/กี่คน
- ## โอกาสใช้ AI/Automation — จัดลำดับความสำคัญ (impact สูง→ต่ำ) แต่ละข้อบอกว่าช่วยงานอะไร กระทบใครบ้าง
- ## ข้อเสนอภาพรวม + Roadmap 3–6 เดือน (แบ่ง Quick Wins / ระยะกลาง / ระยะยาว)
- ## ความเสี่ยงและข้อควรระวัง`;

function buildCompanyContext(interviews) {
  const lines = [];
  lines.push(`วันที่จัดทำรายงาน (ใช้วันที่นี้เท่านั้น): ${todayStr()}`);
  lines.push(`จำนวนพนักงานที่สัมภาษณ์เสร็จแล้ว: ${interviews.length} คน`);
  lines.push('');
  interviews.forEach((iv, i) => {
    const e = iv.employee || {};
    lines.push(
      `[${i + 1}] ${e.name || '-'} — ${e.role || '-'} (ฝ่าย: ${e.division_name || e.division_id || '-'})`
    );
    const role = getAnswer(iv, 'warmup');
    if (role) lines.push(`   บทบาท: ${trunc(role, 200)}`);
    const pain = getAnswer(iv, 'pain_points');
    if (pain) lines.push(`   ปัญหา/เสียเวลา: ${trunc(pain, 300)}`);
    const bot = getAnswer(iv, 'bottlenecks');
    if (bot) lines.push(`   คอขวด: ${trunc(bot, 300)}`);
    const wish = getAnswer(iv, 'ai_wishlist');
    if (wish) lines.push(`   อยากให้ AI ช่วย: ${trunc(wish, 300)}`);
    const kpi = getAnswer(iv, 'own_kpi');
    if (kpi) lines.push(`   ตัวชี้วัดในมุมตัวเอง: ${trunc(kpi, 200)}`);
    lines.push('');
  });
  return lines.join('\n');
}

async function analyzeCompany(interviews) {
  const list = interviews || [];
  if (!list.length) return mock.analyzeCompany(list);
  if (!isEnabled()) return mock.analyzeCompany(list);

  try {
    return await runClaude({
      system: SYS_COMPANY,
      user: buildCompanyContext(list),
      maxTokens: 8000,
    });
  } catch (e) {
    console.error('[claude-ai] analyzeCompany failed — using mock report: ' + e.message);
    return mock.analyzeCompany(list);
  }
}

