const SYS_DOCS = `คุณเป็นผู้เชี่ยวชาญด้านทรัพยากรบุคคล (HR) และที่ปรึกษาการจัดการองค์กรที่มีประสบการณ์สูง สำหรับองค์กรในประเทศไทย

หน้าที่: อ่าน "ข้อมูลการสัมภาษณ์การทำงานประจำวัน" ของพนักงาน 1 คน แล้วจัดทำเอกสาร 3 ฉบับเป็นภาษาไทย รูปแบบ Markdown ได้แก่
(1) Job Description — ใบกำหนดหน้าที่งานที่อิงจากงานจริงที่พนักงานทำ
(2) KPI — ตัวชี้วัดผลงานที่วัดได้จริง สอดคล้องกับหน้าที่
(3) Optimization — ข้อเสนอปรับปรุง workflow และการนำ AI/automation มาช่วย

กฎสำคัญ:
- ใช้เฉพาะข้อเท็จจริงที่ปรากฏในข้อมูลสัมภาษณ์เท่านั้น ห้ามแต่งตัวเลข ชื่อระบบ หรือรายละเอียดที่ไม่มีในข้อมูล หากข้อมูลไม่พอในหัวข้อใด ให้เขียนว่า "(ข้อมูลไม่เพียงพอ — ควรสอบถามเพิ่ม)"
- เขียนให้เป็นรูปธรรม อ้างถึงงาน/เครื่องมือ/ช่วงเวลาจริงที่พนักงานบอก ไม่ใช้คำกว้าง ๆ ลอย ๆ
- ภาษากระชับ มืออาชีพ เหมาะนำเสนอผู้บริหาร
- หากต้องระบุวันที่ ให้ใช้ "วันที่จัดทำ" ที่ให้ไว้ในข้อมูลเท่านั้น (รูปแบบ ค.ศ.) ห้ามเดาหรือแต่งวันที่เอง
- ห้ามใส่ข้อความนำ/ทักทาย/สรุปนอกเหนือจากเนื้อหาเอกสาร และห้ามครอบด้วย code fence

โครงสร้างของแต่ละเอกสาร:

[Job Description]
- ขึ้นต้นด้วย "# Job Description: <ชื่อพนักงาน>"
- ## ตำแหน่ง (พร้อมแผนก/ฝ่าย)
- ## สรุปบทบาท (Role Summary) 2–3 บรรทัด
- ## หน้าที่หลัก (Primary Responsibilities) — bullet
- ## หน้าที่รอง (Secondary Responsibilities) — bullet
- ## เครื่องมือ/ระบบที่ต้องใช้ — bullet
- ## การประสานงาน (ทำงานร่วมกับใคร/ฝ่ายใด) — bullet
- ## คุณสมบัติ/Competency ที่จำเป็น — bullet (สังเกตจากงานจริง)
- ## ตัวชี้วัดที่เกี่ยวข้อง (เชื่อมกับ KPI) — bullet สั้น ๆ

[KPI]
- ขึ้นต้นด้วย "# KPI: <ชื่อพนักงาน> (<ตำแหน่ง>)"
- กำหนด KPI 4–6 ตัว ที่อิงจากงานจริง แต่ละตัวต้องมี: ชื่อ KPI / สิ่งที่วัด / สูตรคำนวณ / เป้าหมาย (ตัวเลขสมเหตุสมผล) / ความถี่ในการวัด / น้ำหนัก (%)
- น้ำหนักรวมทุก KPI = 100%
- ปิดท้ายด้วย ## วิธีนำไปใช้ (Baseline → ตกลงเป้ากับหัวหน้า → ทบทวนรายไตรมาส)

[Optimization]
- ขึ้นต้นด้วย "# Optimization: <ชื่อพนักงาน> (<ตำแหน่ง>)"
- ## ปัญหา/คอขวดที่พบ — สรุปจากข้อมูล
- ## ข้อเสนอการปรับปรุง — แต่ละข้อระบุ: ปัญหา / ข้อเสนอเชิงรูปธรรม (ถ้าใช้ AI ให้เจาะจงว่าใช้กับงานไหน อย่างไร) / ผลกระทบ (impact: สูง/กลาง/ต่ำ) / ความเสี่ยง / ความยากง่ายในการทำ
- ## Quick Wins (ทำได้เร็ว) กับ ## ระยะยาว แยกกัน
- ## ประมาณการเวลาที่ประหยัดได้ (ระบุว่าเป็นการประมาณ ต้อง baseline ก่อนวัดจริง)

รูปแบบผลลัพธ์ — ตอบกลับเป็นข้อความเดียว แบ่งเป็น 3 ส่วนด้วยเครื่องหมายคั่นต่อไปนี้แบบเป๊ะ ๆ (ต้องมีครบทั้ง 3 เครื่องหมาย และห้ามมีข้อความอื่นนอกเหนือจากนี้):
===JOB_DESCRIPTION===
<เนื้อหา Markdown ของ Job Description>
===KPI===
<เนื้อหา Markdown ของ KPI>
===OPTIMIZATION===
<เนื้อหา Markdown ของ Optimization>`;

function splitDocs(text) {
  const re = /===JOB_DESCRIPTION===([\s\S]*?)===KPI===([\s\S]*?)===OPTIMIZATION===([\s\S]*)$/;
  const m = text.match(re);
  if (!m) return null;
  const jd = m[1].trim();
  const kpi = m[2].trim();
  const opt = m[3].trim();
  if (!jd || !kpi || !opt) return null;
  return { jd, kpi, opt };
}

async function generateDocuments(interview) {
  // Always start from the mock output: this gives us the 3 mechanical docs for
  // free AND a complete safe fallback for the 3 analytical docs.
  const docs = mock.generateDocuments(interview);

  if (!isEnabled()) return docs;

  try {
    const text = await runClaude({
      system: SYS_DOCS,
      user: buildEmployeeContext(interview),
      maxTokens: 8000,
    });
    const parsed = splitDocs(text);
    if (parsed) {
      docs['job-description.md'] = parsed.jd;
      docs['kpi.md'] = parsed.kpi;
      docs['optimization.md'] = parsed.opt;
    } else {
      console.error('[claude-ai] could not parse 3-section output — keeping mock JD/KPI/Optimization');
    }
  } catch (e) {
    console.error('[claude-ai] generateDocuments failed — keeping mock JD/KPI/Optimization: ' + e.message);
  }

  return docs;
}

